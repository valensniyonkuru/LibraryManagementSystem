    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
 if(searchTxt.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Search Book ID is Mandatory");
        }else{
            //call model
            LibrarianSectionModel thelibrarian = new LibrarianSectionModel();
            thelibrarian.setBookid(searchTxt.getText());
            //call dao
            LibrarianSectionDao dao = new LibrarianSectionDao();
            LibrarianSectionModel LibrarianSectionModelObj = dao.searchBook(thelibrarian);
            if(LibrarianSectionModelObj != null){
                bookidTxt.setText(LibrarianSectionModelObj.getBookid());
                booknameTxt.setText(LibrarianSectionModelObj.getBookname());
                authorTxt.setText(LibrarianSectionModelObj.getAuthor());
                publisherTxt.setText(LibrarianSectionModelObj.getPublisher());
                quantityTxt.setText(LibrarianSectionModelObj.getQuantity());
                
                bookidTxt.setEditable(false);
            }else{
                JOptionPane.showMessageDialog(this, "Book doesn't exist");
            }}
    }//GEN-LAST:event_searchBtnActionPerformed

