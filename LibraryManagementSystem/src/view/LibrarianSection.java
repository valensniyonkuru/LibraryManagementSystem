    private void registerTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerTxtActionPerformed
           if(bookidTxt.getText().trim().isEmpty() 
          || booknameTxt.getText().trim().isEmpty()
          || authorTxt.getText().trim().isEmpty() || publisherTxt.getText().trim().isEmpty() || quantityTxt.getText().trim().isEmpty() )
       {
           JOptionPane.showMessageDialog(this, "all Field are required");
       }else{
               
                // Check if quantity is a positive number
        try {
            int quantity = Integer.parseInt(quantityTxt.getText());
            if (quantity < 0) {
                JOptionPane.showMessageDialog(this, "Quantity should be a positive number");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Quantity should be a valid number");
            return;
        }
            // 2 check if Book is 5 characters
            if(bookidTxt.getText().trim().length()==5){
                // call model
                 LibrarianSectionModel thelibrarian = new LibrarianSectionModel();
                  thelibrarian.setBookid(bookidTxt.getText());
                thelibrarian.setBookname(booknameTxt.getText());
                thelibrarian.setAuthor(authorTxt.getText());
                thelibrarian.setPublisher(publisherTxt.getText());
                thelibrarian.setQuantity(quantityTxt.getText());
                 //call dao
                LibrarianSectionDao dao= new LibrarianSectionDao();
                String feedBack = dao.registerBook(thelibrarian);
                JOptionPane.showMessageDialog(this, feedBack);
          } else{
                JOptionPane.showMessageDialog(this, " book ID should be 5  characters", "Character Range", JOptionPane.WARNING_MESSAGE);
            } }      
    }//GEN-LAST:event_registerTxtActionPerformed

